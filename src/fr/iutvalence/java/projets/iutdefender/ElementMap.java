package fr.iutvalence.java.projets.iutdefender;

/** ElementMap: les cosntantes de la map sous forme énumérées*/ 
	public enum ElementMap{
		
		/**
		 * represente une parcelle constructible dans la map.
		 */
		CONSTRUCTIBLE, 
		/**
		 * represente une parcelle de chemin dans la map.
		 */
		CHEMIN,
		/**
		 * represente le départ dans la map.
		 */
		DEPART,
		/**
		 * represente l'arrivée dans la map.
		 */
		ARRIVE,
		/**
		 * represente une tour dans la map.
		 */
		TOUR,		
		/**
		 * représente l'absence d'éléments dans la map.
		 */
		VIDE,
	}
	
	
	
